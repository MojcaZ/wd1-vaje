
mood = input("What is your mood today? \n")

if mood == "happy":
    print("It's great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("I hope you feel better soon.")
elif mood == "excited":
    print("Yay!")
elif mood == "relaxed":
    print("Today it's a nice day.")
else:
    print("I don't recognise this mood.")